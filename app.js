// Q1 : write a function 'checkEvenOdd' that accepts a number and prints whether it is even or odd?
// solution:
function checkEvenOdd(num) {
    if (num % 2 == 0) {
        console.log("It's an even number.");
    }
    else {
        console.log("It's an odd number.");
    }
}
checkEvenOdd(11);
